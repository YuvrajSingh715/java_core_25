package com.persistent.flow.control.selection.Stat;

public class SwitchShopMenuEx5 {

	public static void main(String[] args) {

		byte o = 3;

		switch (o) {

		case 1:
			System.out.println("Pizza Price: 240 Rs.");
			break;
		case 2:
			System.out.println("Burger Price: 190 Rs.");
			break;
		case 3:
			System.out.println("Sandwich Price: 220 Rs.");
			break; 
		case 4:
			System.out.println("Coffee Price: 180 Rs");
			break;
		default:
			System.out.println("This item is not available");

		}
	}

}
