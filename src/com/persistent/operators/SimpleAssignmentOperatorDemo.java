package com.persistent.operators;

public class SimpleAssignmentOperatorDemo {

	public static void main(String[] args) {

		byte B = 125; // The byte value is assigned to the variable B
		short S = 25000; // The short value is assigned to the variable S
		int I = 1234453; // The integer value is assigned to the variable I
		long L = 854930589384L; // The long value is assigned to the variable L
		float F = 544.545F; // The float value is assigned to the variable F
		double D = 2335432.9743824734; // The double value is assigned to the variable D
		char C = 'A'; // The char value is assigned to the variable C
		boolean B1 = true; // The boolean value is assigned to the variable B1
		String S1 = "Yuvi"; // The string value is assigned to the variable S1

		
		System.out.println("Byte value = " + B);
		System.out.println("Short value = " + S);
		System.out.println("Integer value = " + I);
		System.out.println("Long value = " + L);
		System.out.println("Float value = " + F);
		System.out.println("Double value = " + D);
		System.out.println("Char value = " + C);
		System.out.println("Boolean value = " + B1);
		System.out.println("String vlaue = " + S1);
		
	}

}
