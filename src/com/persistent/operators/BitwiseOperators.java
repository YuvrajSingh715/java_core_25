package com.persistent.operators;

public class BitwiseOperators {

	public static void main(String[] args) {
		
		        int num1 = 5;  // Binary: 0101
		        int num2 = 3;  // Binary: 0011

		        System.out.println("num1 & num2 = " + (num1 & num2)); // Bitwise AND: 1 (0001)
		        System.out.println("num1 | num2 = " + (num1 | num2)); // Bitwise OR: 7 (0111)
		        
	    }
		        
	}


